/**
 * scripts/build-lemma-pos-map.js
 *
 * Run with: node scripts/build-lemma-pos-map.js
 *
 * Reads the vocab banks and writes a browser-ready
 * js/lemma-pos-map.js file exposing:
 *   window.LEMMA_POS_MAP = { lemmaOrForm: "verb" | "noun" | "adj" | "adv" | ... }
 */

const fs = require('fs');
const path = require('path');

function loadJson(relativePath) {
  const fullPath = path.join(__dirname, '..', relativePath);
  const raw = fs.readFileSync(fullPath, 'utf8');
  return JSON.parse(raw);
}

// Load vocab banks
const spokenNgsl = loadJson('learning-resources/vocab/spoken-ngsl/core.json');
const longmanComm = loadJson('learning-resources/vocab/longman-comm3000/core.json');
const oxford3000 = loadJson('learning-resources/vocab/oxford3000/core.json');

/**
 * Normalise a POS string to one of:
 *   verb, noun, adj, adv, other
 */
function normalisePos(rawPos) {
  if (!rawPos) return null;
  const p = String(rawPos).toLowerCase();

  if (p.startsWith('v')) return 'verb';
  if (p.startsWith('n')) return 'noun';
  if (p.startsWith('adj')) return 'adj';
  if (p.startsWith('adv')) return 'adv';

  // Common labels from Oxford/Longman
  if (p === 'verb' || p === 'v') return 'verb';
  if (p === 'noun' || p === 'n') return 'noun';
  if (p === 'adjective' || p === 'a') return 'adj';
  if (p === 'adverb') return 'adv';

  return 'other';
}

const map = Object.create(null);

/**
 * Helper to set lemma/form -> pos with priority:
 *   NGSL > Oxford > Longman
 */
function setPos(key, pos, priority) {
  if (!key || !pos) return;
  const k = key.toLowerCase();

  const existing = map[k];
  if (!existing) {
    map[k] = { pos, priority };
    return;
  }

  // Keep value if existing has higher priority (lower number)
  if (existing.priority <= priority) return;

  map[k] = { pos, priority };
}

// 1) From NGSL (highest priority)
if (Array.isArray(spokenNgsl.entries)) {
  spokenNgsl.entries.forEach((e) => {
    const lemma = (e.lemma || '').toLowerCase();
    if (!lemma) return;

    const pos = normalisePos(e.pos);
    if (!pos) return;

    setPos(lemma, pos, 1);

    if (Array.isArray(e.forms)) {
      e.forms.forEach((f) => {
        setPos(f, pos, 1);
      });
    }
  });
}

// 2) From Oxford 3000 (second priority)
if (Array.isArray(oxford3000.entries)) {
  oxford3000.entries.forEach((e) => {
    const lemma = (e.lemma || '').toLowerCase();
    if (!lemma) return;

    const pos = normalisePos(e.wordClass || e.pos);
    if (!pos) return;

    setPos(lemma, pos, 2);
  });
}

// 3) From Longman (third priority)
if (Array.isArray(longmanComm.entries)) {
  longmanComm.entries.forEach((e) => {
    const lemma = (e.lemma || '').toLowerCase();
    if (!lemma) return;

    const pos = normalisePos(e.wordClass || e.pos || e.partOfSpeech);
    if (!pos) return;

    setPos(lemma, pos, 3);
  });
}

// Flatten to simple lemma -> pos object for the browser
const clientMap = Object.create(null);
Object.keys(map).forEach((k) => {
  clientMap[k] = map[k].pos;
});

// Emit js/lemma-pos-map.js
const outDir = path.join(__dirname, '..', 'js');
if (!fs.existsSync(outDir)) {
  fs.mkdirSync(outDir, { recursive: true });
}

const outPath = path.join(outDir, 'lemma-pos-map.js');
const banner = `// Auto-generated by scripts/build-lemma-pos-map.js
// Do not edit by hand.

`;

const jsContent =
  banner +
  'window.LEMMA_POS_MAP = ' +
  JSON.stringify(clientMap, null, 2) +
  ';\n';

fs.writeFileSync(outPath, jsContent, 'utf8');
console.log(`Wrote ${outPath} with ${Object.keys(clientMap).length} entries.`);
